package com.eugenefe.feed;

public class QDataSource {
	private String id;
	private String host;
	private String description;
	private String name;
	private String code;
	private int	datasets_count;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getDatasets_count() {
		return datasets_count;
	}
	public void setDatasets_count(int datasets_count) {
		this.datasets_count = datasets_count;
	}
	
	
}
